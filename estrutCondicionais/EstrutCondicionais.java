package estrutcondicionais;

public class EstrutCondicionais {

	public static void main(String[] args) {
		
		int nota = 43;
		
		/*
			se a nota do aluno for maior ou igual a 70 
			então o aluno está aprovado, senão está reprovado
		*/
		if(nota >= 70) {
			System.out.println("Aluno Aprovado!");
		} else {
			System.out.println("Aluno Reprovado!");
		}
	}
}
